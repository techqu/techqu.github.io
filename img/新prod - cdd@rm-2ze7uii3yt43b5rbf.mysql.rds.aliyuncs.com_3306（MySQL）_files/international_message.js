/**
 * 字符串占位符格式化
 * Javascript 使用 位置参数 或者 json来做文案格式化：
 *
 * 多个单独的参数：，占位符号必须是从0开始的数字
 * 'name {1}, code: {0}, alias: {2}'.format('dms', '数据管理', 'idbcloud');

 * 一个数据参数，占位符号必须是从0开始的数字
 * 'name {1}, code: {0}, alias: {2}'.format(['dms', '数据管理', 'idbcloud']);

 * 一个JSON 参数，占位符号可以是普通单词
 * 'name {1}, code: {code}, alias: {alias}'.format({alias:'idbcloud', code:'dms', 1:'数据管理'});
 * @returns {*}
 */
String.prototype.format = function() {
	var args = Array.prototype.slice.apply(arguments).slice();

	if(!this.length || args.length < 1) {
		return this;
	}

	var arg0 = args[0];
	var arg0Type = Object.prototype.toString.call(arg0);

	var argObject = {};

	if('[object Object]' == arg0Type) {
		argObject = arg0;
	} else {
		if('[object Array]' == arg0Type) {
			args = arg0;
		}

		for(var ari = 0; ari < args.length; ari++) {
			argObject[ari] = args[ari];
		}
	}

	var pattern = /{\s*(\w+)\s*}/g;

	var replaced = this.replace(pattern, function(match, tag, pos){
		var value = argObject[tag];
		if(value == undefined) {
			value = match;
		}

		return value;
	});

	return replaced;
};


/**
 * 字符串首字母大写
 * this is dms ==> This is dms
 * @returns {string}
 */
String.prototype.capitalize = function() {
	return this.charAt(0).toUpperCase() + this.substr(1);
};

/**
 * 字符串首字母小写
 * This IS DMS ==> this IS DMS
 * @returns {string}
 */
String.prototype.uncapitalize = function() {
	return this.charAt(0).toLowerCase() + this.substr(1);
}

/**
 * 字符串中所有单词首字母大写
 * this is dms ==> This Is Dms
 * @returns {string}
 */

String.prototype.capitalizeWord = function() {
	return this.replace(/\b[a-z]/g, function(matched) {
		return matched.toUpperCase();
	})
};

/**
 * 字符串中所有单词首字母小写
 * This IS DMS ==> this iS dMS
 * @returns {string}
 */

String.prototype.uncapitalizeWord = function() {
	return this.replace(/\b[A-Z]/g, function(matched) {
		return matched.toLowerCase();
	})
};



